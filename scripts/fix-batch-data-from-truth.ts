async function main() {
  console.log(`\n=== 정답 기준 배치 데이터 수정 (v2) ===`);
  console.log(`모드: ${DRY_RUN ? 'DRY RUN' : '실행'}`);
  console.log(`테넌트: ${TENANT_ID}`);
  console.log(`정답 레코드: ${TRUTH_DATA.length}건\n`);

  const conn = await mysql.createConnection(DB_URL);

  // 1. v2 제품명 → id 매핑 빌드
  const [products] = await conn.execute(
    'SELECT id, product_name FROM h_products_v2 WHERE tenant_id = ?',
    [TENANT_ID]
  ) as any[];
  
  const nameToId = new Map<string, number>();
  for (const p of products) {
    nameToId.set(p.product_name, p.id);
  }
  console.log(`v2 제품 수: ${nameToId.size}`);

  // 제품명 별칭 확인
  for (const [alias, dbName] of Object.entries(NAME_ALIASES)) {
    const pid = nameToId.get(dbName);
    if (pid) {
      nameToId.set(alias, pid);
      console.log(`  별칭: ${alias} → ${dbName} (id=${pid})`);
    } else {
      console.log(`  ⚠️ 별칭 DB 이름 없음: ${dbName}`);
    }
  }

  // 2. 정답 제품명 확인 (모두 매핑 가능한지)
  const missingProducts = new Set<string>();
  for (const [, name] of TRUTH_DATA) {
    if (!nameToId.has(name)) {
      missingProducts.add(name);
    }
  }
  if (missingProducts.size > 0) {
    console.log(`\n❌ 매핑 불가 제품 ${missingProducts.size}건:`);
    for (const p of missingProducts) console.log(`  - ${p}`);
    console.log('먼저 h_products_v2에 제품을 등록하세요.');
    await conn.end();
    process.exit(1);
  }
  console.log('✅ 모든 정답 제품명 매핑 가능');

  // 3. 현재 배치 데이터 로드 (날짜+id 순)
  const [batches] = await conn.execute(
    `SELECT b.id, DATE_FORMAT(b.planned_date, '%Y-%m-%d') as planned_date, 
            b.product_id, p.product_name, b.planned_quantity
     FROM h_batches b
     LEFT JOIN h_products_v2 p ON b.product_id = p.id
     WHERE b.tenant_id = ?
     ORDER BY b.planned_date, b.id`,
    [TENANT_ID]
  ) as any[];
  console.log(`현재 배치 수: ${batches.length}`);

  // 4. DB 배치를 날짜별 그룹화
  const batchesByDate = new Map<string, any[]>();
  for (const b of batches) {
    const d = String(b.planned_date);
    if (!batchesByDate.has(d)) batchesByDate.set(d, []);
    batchesByDate.get(d)!.push(b);
  }

  // 5. 정답도 날짜별 그룹화 (순서 유지)
  const truthByDate = new Map<string, { name: string; qty: number; pid: number }[]>();
  for (const [date, name, qty] of TRUTH_DATA) {
    if (!truthByDate.has(date)) truthByDate.set(date, []);
    truthByDate.get(date)!.push({ name, qty, pid: nameToId.get(name)! });
  }

  // 6. 날짜별 1:1 매칭 및 수정
  let totalUpdated = 0;
  let totalProductFix = 0;
  let totalQtyFix = 0;
  let totalDateFix = 0;
  let totalSkipped = 0;

  for (const [truthDate, truthItems] of truthByDate) {
    // DATE_MAPPING: 정답 날짜가 DB에서 다른 날짜로 저장되어 있는 경우
    // 이미 수정된 경우를 대비해 truthDate와 mapped date 모두 확인
    const mappedDate = DATE_MAPPING[truthDate];
    const dbDate = mappedDate && batchesByDate.has(mappedDate) ? mappedDate : truthDate;
    const dbBatches = batchesByDate.get(dbDate);
    
    if (!dbBatches) {
      console.log(`  ❌ ${truthDate}: DB에 배치 없음`);
      continue;
    }

    if (dbBatches.length !== truthItems.length) {
      console.log(`  ❌ ${truthDate}: 배치 수 불일치 (정답=${truthItems.length}, DB=${dbBatches.length})`);
      continue;
    }

    // 최적 매칭: Hungarian algorithm 대신 greedy로 충분
    // 전략: 정답 각 항목에 대해 가장 적합한 DB 배치 매칭
    const usedBatchIds = new Set<number>();
    const assignments: { truth: typeof truthItems[0]; batch: any }[] = [];

    for (const truthItem of truthItems) {
      // 1순위: 제품+수량 정확 일치
      let match = dbBatches.find(b => 
        !usedBatchIds.has(b.id) && 
        b.product_id === truthItem.pid && 
        Math.abs(Number(b.planned_quantity) - truthItem.qty) < 0.05
      );
      // 2순위: 제품만 일치
      if (!match) {
        match = dbBatches.find(b => 
          !usedBatchIds.has(b.id) && 
          b.product_id === truthItem.pid
        );
      }
      // 3순위: 수량만 일치
      if (!match) {
        match = dbBatches.find(b => 
          !usedBatchIds.has(b.id) && 
          Math.abs(Number(b.planned_quantity) - truthItem.qty) < 0.05
        );
      }
      // 4순위: 아무거나
      if (!match) {
        match = dbBatches.find(b => !usedBatchIds.has(b.id));
      }

      if (match) {
        usedBatchIds.add(match.id);
        assignments.push({ truth: truthItem, batch: match });
      }
    }

    // 할당된 매칭에 대해 UPDATE 실행
    for (const { truth, batch } of assignments) {
      const needDateFix = dbDate !== truthDate;
      const needProductFix = batch.product_id !== truth.pid;
      const needQtyFix = Math.abs(Number(batch.planned_quantity) - truth.qty) > 0.05;

      if (!needDateFix && !needProductFix && !needQtyFix) {
        totalSkipped++;
        continue;
      }

      const changes: string[] = [];
      if (needDateFix) changes.push(`날짜 ${dbDate}→${truthDate}`);
      if (needProductFix) changes.push(`제품 ${batch.product_name}→${truth.name}`);
      if (needQtyFix) changes.push(`수량 ${batch.planned_quantity}→${truth.qty}`);

      console.log(`  수정: batch ${batch.id} (${dbDate}): ${changes.join(', ')}`);

      if (!DRY_RUN) {
        await conn.execute(
          'UPDATE h_batches SET planned_date = ?, product_id = ?, planned_quantity = ? WHERE id = ?',
          [truthDate, truth.pid, truth.qty, batch.id]
        );
      }

      if (needDateFix) totalDateFix++;
      if (needProductFix) totalProductFix++;
      if (needQtyFix) totalQtyFix++;
      totalUpdated++;
    }
  }

  await conn.end();

  console.log(`\n=== 요약 ===`);
  console.log(`총 수정: ${totalUpdated}건`);
  console.log(`  - 제품ID 수정: ${totalProductFix}건`);
  console.log(`  - 수량 수정: ${totalQtyFix}건`);
  console.log(`  - 날짜 수정: ${totalDateFix}건`);
  console.log(`변경 없음: ${totalSkipped}건`);
  console.log(`합계: ${totalUpdated + totalSkipped}건 / ${TRUTH_DATA.length}건`);
  console.log(`\n모드: ${DRY_RUN ? 'DRY RUN (실제 변경 없음)' : '실행 완료'}`);
}

main().catch(e => { console.error(e); process.exit(1); });
