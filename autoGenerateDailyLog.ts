/**
 * 배치 완료 시 일일일지(daily_logs) 자동 생성 서비스 v2
 *
 * 규칙:
 * - 그날의 **첫 번째** 배치 완료 시만 신규 생성 (하루 1건)
 * - 이후 같은 날 배치 완료는 기존 일지에 배치 정보 누적 기록만 추가
 * - 생성 시 모든 체크 항목 기본값 = '적합'(pass)
 * - 온/습도, 냉동고, 냉장고 온도는 빈 행으로 생성 (추후 작성)
 * - 생성 직후 status = 'submitted' + 작성자 자동 승인 처리 → pending_review 전환
 *
 * HACCP 문서 기준 (일일일지 수정.pdf 5페이지 구성):
 * Page1: 일반위생관리 및 공정점검표 (daily_log_hygiene_checks)
 * Page2: 이물관리 점검표 (daily_log_foreign_material_checks)
 * Page3: 원재료실 온/습도 점검기록지 (daily_log_temperature_humidity)
 * Page4: 급속냉동고/냉동고 온도 점검기록지 (daily_log_freezer_temperature)
 * Page5: 원재료 냉장고 온도 점검기록지 (daily_log_refrigerator_temperature)
 */

