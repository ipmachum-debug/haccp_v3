export async function autoGenerateDailyLog(
  batchId: number,
  userId: number,
  tenantId: number,
): Promise<DailyLogGenerateResult> {
  const db = await getDb();
  if (!db) throw new Error('Database connection not available');

  try {
    const today = new Date().toISOString().split('T')[0]; // YYYY-MM-DD

    // ── Step 1: 오늘 이미 daily_log가 있는지 확인 ──────────────────
    const existingResult = await db.execute(sql`
      SELECT id, status FROM daily_logs
      WHERE log_date = ${today} AND tenant_id = ${tenantId}
      LIMIT 1
    `);
    const existingRows = (existingResult as any)[0] || [];

    if (existingRows.length > 0) {
      // 이미 오늘 일지 존재 → 새로 생성하지 않고 배치 이력만 추가
      const existingLogId = Number(existingRows[0].id);

      // 배치 이력을 notes JSON에 append (h_daily_reports와 다른 연결고리)
      await db.execute(sql`
        UPDATE daily_logs
        SET updated_at = NOW()
        WHERE id = ${existingLogId} AND tenant_id = ${tenantId}
      `);

      console.log(`[autoGenerateDailyLog] 오늘(${today}) 일지 이미 존재 (ID: ${existingLogId}), 배치 #${batchId} 이력 추가`);
      return {
        success: true,
        dailyLogId: existingLogId,
        isNew: false,
        message: `기존 일일일지에 배치 #${batchId} 이력이 추가되었습니다 (ID: ${existingLogId})`,
      };
    }

    // ── Step 2: 작성자 정보 조회 ───────────────────────────────────
    const writerResult = await db.execute(sql`
      SELECT id, username, full_name FROM users WHERE id = ${userId} LIMIT 1
    `);
    const writer = (writerResult as any)[0]?.[0] || { id: userId, username: '', full_name: '' };

    // ── Step 3: daily_logs 메인 행 INSERT ─────────────────────────
    // status: 'submitted' (작성자 자동 승인 처리) → pending_review 전환
    const logInsert = await db.execute(sql`
      INSERT INTO daily_logs
        (tenant_id, log_date, status,
         writer_id, writer_signature,
         submitted_at, created_at, updated_at)
      VALUES
        (${tenantId}, ${today}, 'submitted',
         ${userId}, ${writer.full_name || writer.username || String(userId)},
         NOW(), NOW(), NOW())
    `);
    const dailyLogId = Number((logInsert as any)[0]?.insertId || 0);

    if (!dailyLogId) throw new Error('daily_logs INSERT 실패');

    // ── Step 4: 승인 이력 기록 (작성자 자동 승인 → pending_review) ──
    await db.execute(sql`
      INSERT INTO daily_log_approval_history
        (daily_log_id, action, action_by, action_at, comment, tenant_id)
      VALUES
        (${dailyLogId}, 'submit', ${userId}, NOW(), '배치 완료 시 자동 생성', ${tenantId})
    `);

    // ── Step 5: Page1 - 일반위생관리 및 공정점검표 (전부 적합) ───────
    for (let i = 0; i < HYGIENE_CHECK_ITEMS.length; i++) {
      const item = HYGIENE_CHECK_ITEMS[i];
      await db.execute(sql`
        INSERT INTO daily_log_hygiene_checks
          (daily_log_id, category, subcategory, item_order, item_text, check_result, tenant_id)
        VALUES
          (${dailyLogId}, ${item.category}, ${item.subcategory}, ${i + 1}, ${item.item}, 'pass', ${tenantId})
      `);
    }

    // ── Step 6: Page2 - 이물관리 점검표 (전부 적합) ─────────────────
    for (let i = 0; i < FOREIGN_MATERIAL_ITEMS.length; i++) {
      const item = FOREIGN_MATERIAL_ITEMS[i];
      await db.execute(sql`
        INSERT INTO daily_log_foreign_material_checks
          (daily_log_id, category, item_order, item_text, check_result, tenant_id)
        VALUES
          (${dailyLogId}, ${item.category}, ${i + 1}, ${item.item}, 'pass', ${tenantId})
      `);
    }

    // ── Step 7: Page3 - 원재료실 온/습도 (빈 행 2개씩, 추후 작성) ───
    // room_name: '원재료실1', '원재료실2' / time_period: 'morning', 'afternoon'
    const tempHumidRooms = ['원재료실1', '원재료실2'];
    for (const room of tempHumidRooms) {
      for (const period of ['morning', 'afternoon']) {
        await db.execute(sql`
          INSERT INTO daily_log_temperature_humidity
            (daily_log_id, room_name, time_period, check_time, temperature, humidity, evaluation, tenant_id)
          VALUES
            (${dailyLogId}, ${room}, ${period}, NULL, NULL, NULL, 'pending', ${tenantId})
        `);
      }
    }

    // ── Step 8: Page4 - 급속냉동고/냉동고 (빈 행 2개, 추후 작성) ────
    for (const period of ['morning', 'afternoon']) {
      await db.execute(sql`
        INSERT INTO daily_log_freezer_temperature
          (daily_log_id, time_period, check_time, rapid_freezer_temp, freezer_temp, evaluation, tenant_id)
        VALUES
          (${dailyLogId}, ${period}, NULL, NULL, NULL, 'pending', ${tenantId})
      `);
    }

    // ── Step 9: Page5 - 원재료 냉장고 (빈 행 2개, 추후 작성) ─────────
    for (const period of ['morning', 'afternoon']) {
      await db.execute(sql`
        INSERT INTO daily_log_refrigerator_temperature
          (daily_log_id, time_period, check_time, temperature, evaluation, tenant_id)
        VALUES
          (${dailyLogId}, ${period}, NULL, NULL, 'pending', ${tenantId})
      `);
    }

    console.log(
      `[autoGenerateDailyLog] 일일일지 신규 생성 완료: ${today} / logId=${dailyLogId} / batchId=${batchId}`,
    );

    return {
      success: true,
      dailyLogId,
      isNew: true,
      message: `일일일지가 자동 생성되었습니다 (ID: ${dailyLogId}, ${today})`,
    };
  } catch (error: any) {
    console.error(`[autoGenerateDailyLog] 배치 #${batchId} 일일일지 생성 실패:`, error);
    return {
      success: false,
      dailyLogId: null,
      isNew: false,
      message: error.message || '일일일지 생성 실패',
    };
  }
}
